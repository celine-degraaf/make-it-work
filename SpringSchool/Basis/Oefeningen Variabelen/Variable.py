# 3. Temperaturen van vandaag en gisteren opslaan en gemiddelde printen
temperatuur_vandaag = 14.0
temperatuur_gisteren = 13.5  # Je kunt deze waarde aanpassen als je wilt
gemiddelde_temperatuur = (temperatuur_vandaag + temperatuur_gisteren) / 2

print(f"De gemiddelde temperatuur is: {gemiddelde_temperatuur:.1f} graden Celsius.")